// ── Core I18n object ──────────────────────────────────────────────
let _lang = 'pt';

const I18n = {
  get lang() { return _lang; },

  t(key) {
    return TRANSLATIONS[_lang]?.[key] ?? TRANSLATIONS.pt?.[key] ?? key;
  },

  apply() {
    document.documentElement.lang = _lang;
    const year = String(new Date().getFullYear());

    document.querySelectorAll('[data-i18n]').forEach(el => {
      el.innerHTML = this.t(el.dataset.i18n).replace('{year}', year);
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      el.placeholder = this.t(el.dataset.i18nPlaceholder);
    });

    document.querySelectorAll('[data-i18n-aria-label]').forEach(el => {
      el.setAttribute('aria-label', this.t(el.dataset.i18nAriaLabel));
    });

    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.lang === _lang);
      btn.setAttribute('aria-pressed', String(btn.dataset.lang === _lang));
    });

    const { i18nTitle, i18nDescription } = document.documentElement.dataset;
    if (i18nTitle)       document.title = this.t(i18nTitle);
    if (i18nDescription) {
      const meta = document.querySelector('meta[name="description"]');
      if (meta) meta.content = this.t(i18nDescription);
    }
  },

  setLang(lang, persist = true) {
    if (!TRANSLATIONS[lang] || lang === _lang) return;
    _lang = lang;
    if (persist) localStorage.setItem('gsos_lang', lang);
    this.apply();
    window.dispatchEvent(new CustomEvent('gsos:lang-changed'));
  },

  // Instant: localStorage → browser navigator
  detectSync() {
    const saved = localStorage.getItem('gsos_lang');
    if (saved && TRANSLATIONS[saved]) { _lang = saved; return; }

    const browser = (navigator.language ?? '').slice(0, 2);
    if (TRANSLATIONS[browser]) { _lang = browser; return; }

    _lang = 'pt';
  },

  phoneName(ptName) {
    if (_lang === 'pt') return ptName;
    const map = PHONE_NAMES[_lang];
    if (!map) return ptName;
    if (map[ptName]) return map[ptName];
    // Normalized fallback: case + accent insensitive
    const norm = s => s.toLowerCase().normalize('NFD').replace(/[̀-ͯ]/g, '');
    const key  = norm(ptName);
    for (const [k, v] of Object.entries(map)) {
      if (norm(k) === key) return v;
    }
    return ptName;
  },

  // Background: IP-based refinement (only when no saved preference)
  async detectFromIP() {
    if (localStorage.getItem('gsos_lang')) return;
    try {
      const ctrl = new AbortController();
      const timer = setTimeout(() => ctrl.abort(), 2500);
      const res = await fetch('https://ipapi.co/json/', { signal: ctrl.signal });
      clearTimeout(timer);
      if (!res.ok) return;
      const data = await res.json();
      const lang = COUNTRY_TO_LANG[data.country_code];
      if (lang && lang !== _lang) this.setLang(lang, false);
    } catch { /* network / timeout — silently ignore */ }
  },
};
